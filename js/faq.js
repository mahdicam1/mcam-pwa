// ===== RENDER FAQ =====
document.addEventListener('DOMContentLoaded', function () {
  renderFAQ();
});

function renderFAQ() {
  const container = document.getElementById('faq-container');
  if (!container) return;
  let html = '';
  faqData.forEach(function (section) {
    html += '<div class="faq-section">';
    html += '<h3 class="faq-section-title">' + section.section + '</h3>';
    section.items.forEach(function (item, idx) {
      html += '<div class="faq-item">';
      html += '<div class="faq-question" onclick="toggleFAQ(this)">' + item.q + '</div>';
      html += '<div class="faq-answer">' + item.a + '</div>';
      html += '</div>';
    });
    html += '</div>';
  });
  container.innerHTML = html;
}

function toggleFAQ(el) {
  const item = el.parentElement;
  item.classList.toggle('open');
}
